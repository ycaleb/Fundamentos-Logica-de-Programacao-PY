## ---Laços de Repetição--- ##

# >>EXECUTAR UM ITEM 20 VEZES UTILIZANDO FOR<< #

for item in range(1, 20):
    # na execução será mostrado na tela de 1 até 19, pois o ultimo numero nao conta
    print(item)

# >>PULOS<< #

for item in range(1, 20, 2):
    # o ultimo 2 reprenta o pulo, ou seja, pula 1 numero em cada repetição
    print(item)
